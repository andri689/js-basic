//fungsi pertambahan
const pertambahan = (number1, number2) => number1 + number2;
console.log(pertambahan(5,5));

//fungsi pengurangan
const pengurangan = (number1, number2) => number1 - number2;
console.log(pengurangan(10,5));

//fungsi pembagian
const pembagian = (number1, number2) => number1 / number2;
console.log(pembagian(25,5));

//fungsi perkalian
const perkalian = (number1, number2) => number1 * number2;
console.log(perkalian(10,5));